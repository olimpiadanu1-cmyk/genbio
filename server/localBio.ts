export function localCheckBio(content: string) {
    const errors: string[] = [];

    // 1. Проверка на нецензурную лексику и оскорбления
    const badWords = ["хуй", "пизд", "ебал", "сука", "бля", "админ", "долбоеб", "шлюх", "еблан"];
    for (const word of badWords) {
        if (content.toLowerCase().includes(word)) {
            errors.push(`Обнаружено недопустимое выражение: "${word}". Пожалуйста, соблюдайте правила приличия.`);
        }
    }

    // 2. Проверка на английские буквы (латиница)
    // Исключаем слово "RolePlay" из проверки
    const contentWithoutRP = content.replace(/RolePlay/gi, "");
    const latinRegex = /[a-zA-Z]/g;
    const matches = contentWithoutRP.match(latinRegex);
    if (matches && matches.length > 5) {
        errors.push("Биография должна быть написана на русском языке. Использование латиницы запрещено.");
    }

    // 3. Проверка на заполнители типа "123" или "йцук"
    const placeholders = ["123", "321", "йцук", "фыва", "qwer", "asdf"];
    for (const p of placeholders) {
        const regex = new RegExp(`\\b${p}\\b`, 'i');
        if (regex.test(content)) {
            errors.push(`Использование заполнителей "${p}" запрещено. Замените их на реальную информацию.`);
        }
    }

    const requiredFields = [
        "Имя Фамилия", "Пол", "Национальность", "Возраст", "Дата и место рождения",
        "Семья", "Место текущего проживания", "Описание внешности", "Особенности характера",
        "Детство", "Юность и взрослая жизнь", "Настоящее время", "Хобби"
    ];

    // 4. Проверка наличия заголовков и ИХ СОДЕРЖИМОГО
    for (const field of requiredFields) {
        const fieldIndex = content.toLowerCase().indexOf(field.toLowerCase());
        if (fieldIndex === -1) {
            errors.push(`Отсутствует обязательное поле или раздел: ${field}`);
        } else {
            // Проверяем, есть ли текст после двоеточия или заголовка
            const textAfter = content.substring(fieldIndex + field.length).trim();
            // Ищем следующий заголовок, чтобы понять границы текущего раздела
            let nextHeaderIndex = -1;
            for (const f of requiredFields) {
                const idx = content.toLowerCase().indexOf(f.toLowerCase(), fieldIndex + 1);
                if (idx !== -1 && (nextHeaderIndex === -1 || idx < nextHeaderIndex)) {
                    nextHeaderIndex = idx;
                }
            }

            const sectionText = nextHeaderIndex === -1
                ? textAfter
                : content.substring(fieldIndex + field.length, nextHeaderIndex).trim();

            // Если в разделе меньше 5 значащих символов (не учитывая ":" и пробелы)
            // Исключение для полей "Возраст" и "Пол", где достаточно 1-2 символов
            const cleanText = sectionText.replace(/[:\-\s]/g, "");
            const minLength = (field === "Возраст" || field === "Пол") ? 1 : 5;

            if (cleanText.length < minLength) {
                errors.push(`Раздел "${field}" не заполнен или содержит слишком мало информации.`);
            }
        }
    }

    const titleRegex = /RolePlay биография гражданина .+/i;
    if (!titleRegex.test(content)) {
        errors.push("Заголовок должен начинаться с 'RolePlay биография гражданина [Имя]'");
    }

    if (content.length < 500) {
        errors.push("Биография слишком короткая. Минимум 500 символов для качественного RP.");
    }

    // 5. Проверка на повествование от первого лица (Heuristic POV check)
    const firstPersonPronouns = ["я", "меня", "мне", "мной", "мною", "мой", "моя", "моё", "мои", "моем", "моём", "моего", "моему", "моими", "моих", "родился", "живу", "работаю", "занимаюсь"];
    const thirdPersonPronouns = ["он", "она", "его", "её", "ее", "ему", "ей", "им", "ею", "о нём", "о нем", "о ней", "они", "их", "ими", "родился", "живет", "работает", "занимается", "проживает"];

    // Функция для подсчета упоминаний (слово целиком)
    const countWords = (text: string, words: string[]) => {
        let count = 0;
        const lowerText = text.toLowerCase();
        for (const word of words) {
            const regex = new RegExp(`\\b${word}\\b`, 'gi');
            const matches = lowerText.match(regex);
            if (matches) count += matches.length;
        }
        return count;
    };

    const firstCount = countWords(content, firstPersonPronouns);
    const thirdCount = countWords(content, thirdPersonPronouns);

    // Если 3-е лицо явно преобладает и его достаточно много
    if (thirdCount > firstCount && thirdCount > 5) {
        errors.push("Биография должна быть написана от первого лица (используя 'Я', 'меня', 'мой' и т.д.). Повествование от третьего лица запрещено.");
    } else if (firstCount < 3) {
        // Если даже 3-го лица мало, но 1-го вообще нет
        errors.push("Похоже, биография написана не от первого лица. Пожалуйста, используйте местоимения 'Я', 'меня', 'мой', чтобы рассказать свою историю.");
    }

    return {
        valid: errors.length === 0,
        errors,
        feedback: errors.length === 0
            ? "Ваша биография отлично составлена и соответствует всем базовым правилам сервера."
            : "Ваша биография содержит ошибки. Пожалуйста, исправьте их."
    };
}

